# Tuples are similar to list
# except they are immutable
# uses () paranthesis

t = (1, 2, 3)
print(t)

# get the element at 0 position
print(t[0])

# get the element at last position
print(t[-1])

new_tuple = ('a', 'a', 'b')
# count a occurence in tuple, O/p is 2
print(new_tuple.count('a'))

# get index value of a, 
print(new_tuple.index('a'))

